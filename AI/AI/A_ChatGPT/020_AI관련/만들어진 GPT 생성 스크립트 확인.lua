· 먼저 웹 브라우저나 데스크톱/모바일 ChatGPT 앱을 연다
· 왼쪽 사이드바 최상단에 있는 “GPTs” 아이콘(또는 “내 GPT” 탭)을 찾아 클릭해
· 화면에 네가 만든 GPT 리스트가 뜨면, 상세 내용을 보고 싶은 GPT 카드 위에 마우스를 올려
· 카드 우측 상단에 …(더보기) 아이콘이 나타나면 그걸 클릭해
· 메뉴 중 “설정 편집(Edit)”을 선택해
· 에디터 화면 왼쪽에는 다음과 같은 섹션들이 있고, 각각 펼쳐보면 실제 스크립트를 볼 수 있어

1. **System message**
   – GPT의 기본 동작을 정의하는 시스템 프롬프트
2. **User instructions**
   – 사용자가 GPT에게 주는 추가 지침
3. **Function definitions**
   – 호출 가능한 함수 목록과 파라미터 스펙
4. **Examples** (선택사항)
   – 샘플 대화 입력·출력 예시
   · 각 블록 우측 상단 펜 모양 아이콘을 클릭하면 내용을 직접 수정하거나 전체를 복사할 수 있어
   · 수정 후 저장하고 싶으면 화면 하단의 “저장” 버튼을 눌러

이렇게 하면 앱 내에서 단 몇 번의 클릭으로 ‘제작 스크립트(시스템 메시지·지침·함수 정의 등)’를 전부 확인·편집할 수 있어.






