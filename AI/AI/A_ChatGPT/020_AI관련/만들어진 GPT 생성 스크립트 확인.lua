지금 내가 생성한 GPT들의 생성 스크립트를 알 수 있을까?



---===---===---===---===---===---> AI 답변 :




ChatGPT에서 직접 만든 GPT들의 “제작 스크립트”(설정된 프롬프트, 시스템 메시지, 함수 호출 규칙 등)를 확인하는 방법은 두 가지가 있어:

1. **ChatGPT 앱 내에서 확인하기**

   1. 왼쪽 사이드바에서 **“GPTs”** 메뉴를 클릭.
   2. 목록에서 보고 싶은 GPT를 선택.
   3. 우측 상단의 ⋯(더보기) 아이콘을 클릭한 뒤 **“설정 편집(Edit)”** 또는 **“Instructions”** 메뉴를 선택.
   4. 여기에 시스템 메시지(system prompt), 에이전트 지침, 함수 정의 등 “제작 스크립트” 전반이 텍스트로 표시돼.

2. **OpenAI API를 통해 프로그래밍 방식으로 가져오기**
   API 키가 있고, Python 환경이 준비돼 있다면 아래 스니펫으로 모든 GPT와 각각의 설정을 조회할 수 있어요.

   ```python
   import openai

   # 환경 변수나 직접 할당으로 API 키 세팅
   openai.api_key = "YOUR_API_KEY"

   # 1) 내가 만든 GPT 목록 불러오기
   gpt_list = openai.GPT.list()
   for g in gpt_list.data:
       print(f"• {g.name} (id: {g.id})")

       # 2) 각 GPT의 상세 정보(스크립트) 조회
       details = openai.GPT.retrieve(g.id)
       print("System prompt:")
       print(details.system_prompt)
       print("User instructions:")
       print(details.user_prompt)
       print("Function definitions:")
       print(details.functions)
       print("="*40)
   ```

   이 코드를 실행하면 터미널에 각 GPT의 시스템 메시지, 사용자 메시지, 함수 호출 설정 등이 쭉 출력됩니다.

---

어떤 방식이든, 위 절차대로 하면 지금까지 생성한 GPT들의 “제작 스크립트”를 모두 살펴볼 수 있을 거예요.





