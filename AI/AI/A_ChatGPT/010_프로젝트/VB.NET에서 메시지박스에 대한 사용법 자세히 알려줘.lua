## VB.NET에서 메시지박스 사용법

VB.NET에서 메시지박스는 사용자에게 정보를 전달하거나, 선택을 요구하는 대화 상자를 띄울 때 주로 사용합니다. 대표적으로 두 가지 방식이 있습니다: `MsgBox` 함수와 `MessageBox` 클래스입니다.

---

**1. MsgBox 함수 사용법**

가장 간단하게 메시지박스를 띄우는 방법은 `MsgBox` 함수를 사용하는 것입니다.

```vbnet
MsgBox("메시지 내용")
```
이렇게 하면 "확인" 버튼만 있는 기본 메시지박스가 나타납니다[2][3][5].

### 주요 인수
- **prompt**: 메시지 내용 (필수)
- **buttons**: 버튼 종류, 아이콘, 기본 버튼 등(선택)
- **title**: 메시지박스의 제목(선택)

#### 예시

```vbnet
' 간단한 메시지박스
MsgBox("작업이 완료되었습니다.")

' 제목이 있는 메시지박스
MsgBox("저장하시겠습니까?", , "저장 확인")

' 버튼과 아이콘 지정 (예/아니오 버튼, 경고 아이콘, 두 번째 버튼이 기본값)
Dim style = MsgBoxStyle.YesNo Or MsgBoxStyle.DefaultButton2 Or MsgBoxStyle.Critical
Dim result = MsgBox("계속하시겠습니까?", style, "확인")
If result = MsgBoxResult.Yes Then
    MsgBox("계속합니다.")
Else
    MsgBox("중단합니다.")
End If
```
- 버튼 및 아이콘 조합은 `MsgBoxStyle` 열거형을 사용합니다[3][5][6].
- 반환값은 사용자가 누른 버튼에 따라 달라지며, `MsgBoxResult`로 비교합니다.

#### 버튼/아이콘 종류

| 상수                | 설명              |
|---------------------|-------------------|
| vbOKOnly            | 확인              |
| vbOKCancel          | 확인, 취소        |
| vbYesNo             | 예, 아니오        |
| vbYesNoCancel       | 예, 아니오, 취소  |
| vbCritical          | 경고(빨간 X) 아이콘|
| vbInformation       | 정보(파란 i) 아이콘|
| vbExclamation       | 느낌표 아이콘      |
| vbQuestion          | 물음표 아이콘      |
| vbDefaultButton2    | 두 번째 버튼 기본값|
| vbApplicationModal  | 현재 앱만 차단    |
| vbSystemModal       | 시스템 전체 차단  |

버튼과 아이콘은 `Or` 연산자로 조합하여 사용할 수 있습니다[5][6].

---

**2. MessageBox 클래스 사용법 (Windows Forms 환경)**

Windows Forms 기반에서는 `System.Windows.Forms.MessageBox` 클래스를 사용할 수 있습니다.

```vbnet
MessageBox.Show("메시지 내용")
```

### 다양한 오버로드

```vbnet
' 제목과 메시지
MessageBox.Show("저장하시겠습니까?", "저장 확인")

' 버튼, 아이콘 지정
Dim res = MessageBox.Show("삭제하시겠습니까?", "삭제 확인", MessageBoxButtons.YesNo, MessageBoxIcon.Warning)
If res = DialogResult.Yes Then
    ' 삭제 실행
End If
```
- `MessageBoxButtons` : OK, OKCancel, YesNo, YesNoCancel 등
- `MessageBoxIcon` : Information, Warning, Error, Question 등
- 반환값은 `DialogResult`로 확인합니다[10].

---

**3. 실전 활용 팁**

- 메시지박스에서 여러 버튼을 사용할 때는 반환값을 변수에 저장해서 분기 처리합니다.
- 중요한 작업(예: 삭제)에서는 기본 버튼을 '아니오'로 설정해 실수 방지[6].
- 메시지박스는 모달 대화상자이므로, 사용자가 닫기 전까지 다른 작업을 할 수 없습니다[10].

---

**요약 예제**

```vbnet
' 예/아니오 버튼, 경고 아이콘, 두 번째 버튼이 기본값
Dim style = MsgBoxStyle.YesNo Or MsgBoxStyle.DefaultButton2 Or MsgBoxStyle.Critical
Dim response = MsgBox("정말로 삭제하시겠습니까?", style, "삭제 확인")
If response = MsgBoxResult.Yes Then
    MsgBox("삭제되었습니다.", MsgBoxStyle.Information, "알림")
Else
    MsgBox("삭제가 취소되었습니다.", MsgBoxStyle.Exclamation, "취소")
End If
```

---

**참고**
- `MsgBox`는 VB.NET과 VBA에서 모두 사용 가능하며, WinForms에서는 `MessageBox.Show`를 더 자주 사용합니다[3][10].
- 버튼, 아이콘, 기본 버튼, 모달 방식 등 다양한 옵션을 조합할 수 있습니다[5][6][10].

