## 1. 회의 목적

주문 시스템에서 ‘쿼테이션(CPQ) 번호’ 검증 기능 도입 및 관련 엑셀 업로드 기능 개발 범위와 우선순위를 확정하기 위함.

---

## 2. 주요 논의 사항

### 2.1 CPQ(쿼테이션) 번호 개념 정리

* **CPQ 번호**: 20으로 시작하는 8자리 숫자
* 제품 거래처별 가격 리스트 식별자이며, 석고보드 등 품목별 가격 정보가 이 번호 아래 관리됨.
* Order 시스템 화면에는 직접 노출되지 않고, 원CRM → SAP 경유 데이터임.

### 2.2 누락 품목 검증 필요성

* 주문 접수 단계에서 CPQ 생성 누락 품목 선택 시 SLPI(전달 시스템)에 누락되어 SAP에 전달되지 않음.
* 이를 방지하기 위해, 주문 시점에 **CPQ 번호 존재 여부를 확인**하고, 누락 건은 **에러 메시지**를 띄워 주문을 차단해야 함.

### 2.3 신규 테이블 및 엑셀 업로드 기능

* **신규 테이블 생성**: CPQ 번호와 제품 코드(Material)를 저장
* **엑셀 업로드**:

  * 고객 측에서 제공된 SAP 추출 엑셀 파일을 시스템에 올림
  * 파일 내 **A열(CPQ 번호)**, **F열(제품 코드)** 만 사용 (컬럼 고정)
* **검증 로직**:

  1. 주문 접수 시 선택된 품목의 제품 코드와 신규 테이블을 조인
  2. 테이블에 없는 CPQ 번호/제품 코드 건이 하나라도 있으면 주문 불가 및 에러 표시

### 2.4 작업 범위 및 우선순위

1. **1차**:

   * CPQ 테이블 구조 설계 및 생성
   * 주문 검증 로직 구현 (누락 시 에러 처리)
2. **2차**:

   * 엑셀 업로드 기능 개발 (수시 업데이트 지원)
3. **추가 기능(추후)**:

   * 납품처·제품 매핑 및 즐겨찾기 기능

