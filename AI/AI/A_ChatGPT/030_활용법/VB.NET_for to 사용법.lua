

=================================> AI에 질문 :


VB.NET에서 for to 사용법에 대해서 알려줘


---===---===---===---===---===---> AI 답변 :




VB.NET의 `For…Next` 문은 정해진 횟수만큼 반복을 수행할 때 쓰는 가장 기본적인 루프야. 문법과 예제를 정리해볼게.

---

## 기본 문법

```vb
For 변수 = 시작값 To 끝값 [Step 증감값]
    ' 반복할 코드
Next [변수]
```

* **변수**: 보통 `Integer` 타입. 루프 카운터로 사용.
* **시작값**, **끝값**: 반복이 시작하고 끝나는 값. 끝값에 도달하거나 넘어가면 루프 종료.
* **Step**: 증감폭. 생략하면 기본값 `1`. 음수로 지정하면 역방향 반복.

---

## 예제

### 1) 0부터 9까지 1씩 증가

```vb
For i As Integer = 0 To 9
    Console.WriteLine(i)
Next
```

* 출력: 0,1,2,…,9

### 2) 10부터 0까지 2씩 감소

```vb
For i As Integer = 10 To 0 Step -2
    Console.WriteLine(i)
Next
```

* 출력: 10,8,6,4,2,0

---

## 중간에 빠져나오기 / 다음 반복으로 건너뛰기

* **Exit For**: 즉시 루프 탈출
* **Continue For**: 남은 본문 건너뛰고 다음 증감으로 이동

```vb
For i As Integer = 1 To 10
    If i = 5 Then
        Exit For        ' i가 5일 때 루프 종료
    End If

    If i Mod 2 = 0 Then
        Continue For    ' 짝수면 출력 건너뛰고 다음 i
    End If

    Console.WriteLine(i)  ' 홀수만 출력: 1,3
Next
```

---

## 중첩 루프

```vb
For i As Integer = 1 To 3
    For j As Integer = 1 To 2
        Console.WriteLine($"i={i}, j={j}")
    Next
Next
```

* i=1 j=1, i=1 j=2, i=2 j=1, … 총 6회 반복

---

## 주의사항

1. **루프 변수 직접 변경 금지**
   `For` 안에서 `i = i - 1`처럼 카운터를 건드리면 무한루프나 예기치 않은 동작이 생겨.
2. **초기값과 끝값 관계**

   * 시작값 < 끝값: `Step`은 양수여야 의미가 있고
   * 시작값 > 끝값: `Step`을 음수로 지정해야 제대로 동작해.

---

이 정도만 잘 익혀두면 `For To`로 대부분의 반복문을 처리할 수 있어. 필요하면 `For Each`나 `While` 루프도 함께 공부해 보면 좋아!





