## 원드라이브(OneDrive)에서 특정 폴더 동기화 취소하는 방법

**윈도우 PC 기준으로 원드라이브의 특정 폴더 또는 모든 폴더 동기화 취소 방법을 정리합니다.**

---

### **1. 특정 폴더 동기화 취소(PC에서)**

원드라이브는 기본적으로 전체 폴더를 동기화하지만, 사용자가 원하는 폴더만 동기화하거나 동기화에서 제외할 수 있습니다.

**단계별 방법:**

1. 작업 표시줄 오른쪽 아래의 원드라이브(구름 모양) 아이콘을 클릭  
2. "도움말 및 설정" → "설정"을 선택  
3. "계정" 탭에서 "폴더 선택" 버튼 클릭  
4. 동기화에서 제외하고 싶은 폴더의 체크를 해제  
5. "확인"을 누르면 해당 폴더는 이 컴퓨터에서 동기화가 중지되고, PC에서는 삭제되지만 클라우드에는 그대로 남아 있습니다[9][5][6].

> 이 방법은 **PC에서만 동기화가 해제**되며, 원드라이브 웹(클라우드)에는 파일이 그대로 남습니다.

---

### **2. 모든 폴더 동기화 취소(PC에서)**

원드라이브 전체 동기화를 멈추고 싶다면 다음과 같이 할 수 있습니다.

- **동기화 일시 중지:**  
  원드라이브 아이콘 클릭 → "도움말 및 설정" → "동기화 일시 중지" 선택(2시간, 8시간, 24시간 중 선택)[9][2][4].

- **PC와 원드라이브 연결 해제:**  
  원드라이브 아이콘 클릭 → "도움말 및 설정" → "설정" → "계정" 탭 → "이 PC 연결 해제" 클릭  
  → 확인 메시지에서 "연결 해제" 선택  
  → 이러면 로컬 폴더는 일반 폴더로 남고, 클라우드와의 동기화가 완전히 중단됩니다[2][6][5].

---

### **3. 스마트폰(모바일)에서 특정 폴더 동기화 취소**

- OneDrive 앱에서 "설정" → "카메라 업로드" → "동기화할 앨범"에서 동기화하지 않을 폴더 해제[8].
- 이미 동기화된 파일을 온라인에서 삭제하면, 스마트폰에서도 삭제될 수 있으니 주의해야 합니다.

---

### **4. 주의사항 및 한계**

- **공유폴더 내 특정 파일만 동기화 해제는 불가:**  
  원드라이브는 공유폴더 내에서 개별 파일만 동기화에서 제외하는 기능을 지원하지 않습니다. 동기화를 원하지 않는 파일은 다른 폴더로 옮겨야 합니다[1][7].
- **동기화 해제 시 파일 삭제:**  
  PC에서 동기화 해제하면 해당 폴더/파일은 PC에서는 삭제되고, 클라우드에는 남아 있습니다.  
  반대로 클라우드에서 삭제하면 PC에서도 삭제될 수 있으니 주의하세요.

