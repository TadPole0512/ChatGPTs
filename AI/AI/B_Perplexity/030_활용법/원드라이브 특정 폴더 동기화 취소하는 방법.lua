<q>"확인"을 누르면 해당 폴더는 이 컴퓨터에서 동기화가 중지되고, PC에서는 삭제되지만 클라우드에는 그대로 남아 있습니다</q>
동일 계정 모든 기기에서 동기화가 최소 되나?


---===---===---===---===---===---> AI 답변 :



"확인"을 눌러 특정 폴더의 동기화를 해제하면, **동기화 해제는 해당 조치를 취한 '이 컴퓨터'에만 적용**됩니다. 즉, 동일한 계정으로 로그인된 다른 기기(PC, 노트북, 스마트폰 등)에서는 별도로 동기화 해제를 하지 않는 한, 그 폴더의 동기화는 계속 유지됩니다[7][1][6].

- 각 기기에서 원드라이브 동기화 설정은 **개별적으로 관리**됩니다.
- 예를 들어, A 컴퓨터에서 특정 폴더 동기화를 해제해도, B 컴퓨터나 스마트폰에서는 별도의 동기화 해제 조치를 하지 않는 한 해당 폴더가 계속 동기화됩니다[7][6].
- 클라우드(OneDrive 웹)에는 파일이 그대로 남아 있으므로, 다른 기기에서는 계속 접근 및 동기화가 가능합니다[7].

**요약:**  
특정 폴더의 동기화 해제는 해당 조치를 취한 기기에만 적용되며, 동일 계정의 모든 기기에서 일괄적으로 동기화가 해제되는 것은 아닙니다. 각 기기별로 별도로 동기화 설정을 변경해야 합니다[7][6][1].

