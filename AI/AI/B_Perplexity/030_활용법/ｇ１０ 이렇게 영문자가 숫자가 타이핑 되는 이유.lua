## 영문자 및 숫자가 자간이 넓어지며 타이핑되는 이유

노트북이나 PC에서 `ｇ１０`처럼 영문자와 숫자가 평소보다 넓은 간격(자간)으로 입력되는 현상은 **입력기(IME) 설정** 또는 **문자 폭(전자/반자) 전환** 때문입니다.

### 주요 원인

- **전자(全角) 모드 활성화**
  - 한글 입력기 또는 일부 일본어/중국어 입력기에서 전자(全角, Full-width) 모드가 활성화되면, 영문자와 숫자가 2바이트 문자로 입력되어 한글이나 한자처럼 넓은 폭으로 보입니다.
  - 예시: 일반적으로 입력되는 'g10' → 전자 모드에서는 'ｇ１０'처럼 넓은 글자 간격으로 입력됨[4][11].

- **입력기 전환 단축키 오작동**
  - Windows에서는 입력기 전환 단축키(예: Alt + =)나 Windows키 + Space, 또는 한컴 입력기 등으로 입력기가 전환될 때 이런 현상이 발생할 수 있습니다[2][4].
  - 한컴 입력기, 일본어 입력기, 중국어 입력기 등에서 전자/반자 전환이 자주 일어납니다.

### 해결 방법

- **입력기 옵션에서 반자(半角, Half-width)로 전환**
  - 한컴 입력기나 기타 입력기의 상태 표시줄에서 '문자 너비' 또는 '전자/반자' 메뉴를 찾아 '반자'로 바꿔주세요[4][11].
  - 일부 입력기는 단축키(예: Alt + =)로 전환됩니다[4].
- **입력기 변경**
  - Windows키 + Space로 Microsoft 기본 입력기로 전환해도 해결됩니다[2].
- **컴퓨터 재부팅**
  - 임시적으로 입력기 오류가 있을 경우 재부팅으로 해결되는 경우도 있습니다[4].

### 참고

- 이 현상은 키보드 고장이나 NumLock 문제와는 별개입니다. NumLock 문제는 영문자가 숫자로 입력되는 현상(예: 'J' → '1')과 관련 있습니다[1][3][5].
- 전자/반자 전환은 입력기에서 문자 폭을 1바이트(좁은 간격, 반자) 또는 2바이트(넓은 간격, 전자)로 바꾸는 기능입니다. 한글, 일본어, 중국어 입력기에서 자주 사용됩니다[4][11].

---

**요약:**  
영문자 및 숫자가 자간이 넓어지며 타이핑되는 것은 입력기가 전자(全角, Full-width) 모드로 전환되어 있기 때문입니다. 입력기 옵션에서 반자(半角, Half-width)로 바꾸거나, 입력기를 Microsoft 기본 입력기로 전환하면 정상적으로 입력됩니다[2][4][11].

