## 노트북에서 영문자가 숫자로 타이핑되는 이유

**현상 설명**  
노트북에서 'g10'처럼 영문자를 입력하려고 할 때 숫자가 입력되는 현상은 주로 키보드의 특정 설정 때문입니다. 예를 들어, 'g'를 입력하면 'g' 대신 '6'이 입력되는 식입니다.

**주요 원인: NumLock 기능 활성화**

- 노트북에는 공간 절약을 위해 별도의 숫자키패드(NumPad)가 없는 모델이 많습니다.
- 이런 노트북에서는 키보드의 일부 영문자 키(M, J, K, L, U, I, O 등)에 숫자 기능이 겸용으로 할당되어 있습니다[1][2].
- 이때 **NumLock** 기능이 켜지면, 해당 키를 눌렀을 때 영문자가 아니라 숫자가 입력됩니다.
- 예를 들어, 'J'를 누르면 '1', 'K'를 누르면 '2', 'L'을 누르면 '3'이 입력되는 식입니다[1].

**원인 요약**
- NumLock이 켜진 상태에서 영문자 키를 누르면, 해당 위치에 할당된 숫자가 입력됨[2].
- 이 기능은 노트북에서 숫자 입력을 쉽게 하기 위한 설계입니다.

**해결 방법**

- **Fn + NumLock** (혹은 Fn + 특정 잠금키, 예: F11) 조합을 눌러 NumLock을 해제하면 정상적으로 영문자가 입력됩니다[1][2].
- 키보드에 NumLock 표시등이 있다면, 꺼져 있는지 확인하세요.
- 일부 노트북은 NumLock 키가 따로 없고, Fn키와 조합해서만 사용할 수 있습니다.

**추가 참고**
- 만약 이 조치를 해도 문제가 해결되지 않으면, 키보드 하드웨어 고장 가능성도 있으니 점검이 필요합니다[1].

---

**요약**  
노트북에서 영문자가 숫자로 입력되는 현상은 대부분 NumLock 기능이 켜져 있기 때문입니다. Fn키와 NumLock(또는 해당 기능키)을 함께 눌러 해제하면 정상 입력이 가능합니다[1][2].

